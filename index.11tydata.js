function authorList(authors) {
    var list = [];
    authors.forEach((name, i) => {
        if (name == AyushTewari) {
            name = '<span class="self-author">' + name + "</span>";
        }
        if (i == authors.length - 1 && name != etal) {
            list.push("and " + name);
        } else {
            list.push(name);
        }
    });
    return list.join(", ");
}
function authorListEqual(authors, numEqual) {
    var list = [];
    authors.forEach((name, i) => {
        if (name == AyushTewari) {
            name = '<span class="self-author">' + name + "</span>";
        }
        if (i < numEqual) {
            name = name + '*'
        }
        if (i == authors.length - 1) {
            list.push("and " + name);
        } else {
            list.push(name + ",");
        }
    });
    if (numEqual > 1) {
        list.push("(* equal contribution)")
    }
    return list.join(" ");
}

function authorListEqualEnd(authors, numEqual) {
    var list = [];
    authors.forEach((name, i) => {
        if (name == AyushTewari) {
            name = '<span class="self-author">' + name + "</span>";
        }
        if (i > authors.length - numEqual - 1) {
            name = name + '&#8224;'
        }
        if (i == authors.length - 1) {
            list.push("and " + name);
        } else {
            list.push(name + ",");
        }
    });
    if (numEqual > 1) {
        list.push("(&#8224; equal advising)")
    }
    return list.join(" ");
}

